package br.com.fiap.client;

public class Retorno {
	public String mensagem;

	public Retorno() {
		super();
	}

	public Retorno(String mensagem) {
		super();
		this.mensagem = mensagem;
	}

	public String getMensagem() {
		return mensagem;
	}

	public void setMensagem(String mensagem) {
		this.mensagem = mensagem;
	}
	
	
}
